// 不変条件: 開発者ID はハードコード。allowedUserIds（設定画面）は Bot 操作許可用。

// -------------------------------------------------------------------------
// 開発者 ID（ハードコード）
// allowedUserIds は Bot 操作許可（Discord からのメッセージ送信を許可するユーザー）。
// DEVELOPER_IDS は開発者権限（管理コマンドの実行など）。
// -------------------------------------------------------------------------
const DEVELOPER_IDS: ReadonlySet<string> = new Set([
    // 開発者の Discord ユーザー ID
    '386216074095624202',
    // 新しい開発者を追加する場合はここに ID を追加すること
]);

// -------------------------------------------------------------------------
// Public API
// -------------------------------------------------------------------------

/**
 * 指定ユーザーが開発者かどうかを判定する。
 */
export function isDeveloper(userId: string): boolean {
    return DEVELOPER_IDS.has(userId);
}
